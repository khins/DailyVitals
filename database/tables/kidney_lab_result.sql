CREATE TABLE IF NOT EXISTS public.kidney_lab_result (
    kidney_lab_result_id bigserial NOT NULL,
    person_id int8 NOT NULL,
    result_month date NOT NULL,
    albumin numeric(10, 2) NOT NULL,
    npcr numeric(10, 2) NOT NULL,
    potassium numeric(10, 2) NOT NULL,
    wktv numeric(10, 2) NOT NULL,
    calcium numeric(10, 2) NOT NULL,
    phosphorus numeric(10, 2) NOT NULL,
    ipth numeric(10, 2) NOT NULL,
    hemoglobin numeric(10, 2) NOT NULL,
    glucose numeric(10, 2) NOT NULL,
    cholesterol numeric(10, 2) NOT NULL,
    triglycerides numeric(10, 2) NOT NULL,
    bun numeric(10, 2) NOT NULL,
    creatinine numeric(10, 2) NOT NULL,
    notes text NULL,
    created_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT kidney_lab_result_pkey PRIMARY KEY (kidney_lab_result_id),
    CONSTRAINT kidney_lab_result_month_start_check CHECK (result_month = date_trunc('month', result_month)::date),
    CONSTRAINT kidney_lab_result_person_month_unique UNIQUE (person_id, result_month),
    CONSTRAINT kidney_lab_result_albumin_check CHECK (albumin >= 0::numeric),
    CONSTRAINT kidney_lab_result_npcr_check CHECK (npcr >= 0::numeric),
    CONSTRAINT kidney_lab_result_potassium_check CHECK (potassium >= 0::numeric),
    CONSTRAINT kidney_lab_result_wktv_check CHECK (wktv >= 0::numeric),
    CONSTRAINT kidney_lab_result_calcium_check CHECK (calcium >= 0::numeric),
    CONSTRAINT kidney_lab_result_phosphorus_check CHECK (phosphorus >= 0::numeric),
    CONSTRAINT kidney_lab_result_ipth_check CHECK (ipth >= 0::numeric),
    CONSTRAINT kidney_lab_result_hemoglobin_check CHECK (hemoglobin >= 0::numeric),
    CONSTRAINT kidney_lab_result_glucose_check CHECK (glucose >= 0::numeric),
    CONSTRAINT kidney_lab_result_cholesterol_check CHECK (cholesterol >= 0::numeric),
    CONSTRAINT kidney_lab_result_triglycerides_check CHECK (triglycerides >= 0::numeric),
    CONSTRAINT kidney_lab_result_bun_check CHECK (bun >= 0::numeric),
    CONSTRAINT kidney_lab_result_creatinine_check CHECK (creatinine >= 0::numeric),
    CONSTRAINT fk_kidney_lab_result_person FOREIGN KEY (person_id) REFERENCES public.person(person_id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_kidney_lab_result_person_month
    ON public.kidney_lab_result USING btree (person_id, result_month DESC);
